    public void UrunGoruntule(){
        model.setRowCount(0);
        
        ArrayList<Urun> urunler = new ArrayList<Urun>();
        
        urunler = veriTabani.urunGetir();
        
        if(urunler != null){
            for(Urun urun : urunler){
                Object[] eklenecek = {urun.getId(), urun.getKategoriId(), urun.getUrunAd(), urun.getMiktar(),urun.getFiyat()};
                
                model.addRow(eklenecek);
            }
        }
    }
    
