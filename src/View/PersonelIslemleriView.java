    public void PersonelGoruntule(){
        model.setRowCount(0);
        
        ArrayList<Personel> personeller = new ArrayList<Personel>();
        
        personeller = veriTabani.personelGetir();
        
        if(personeller != null){
            for(Personel personel : personeller){
                Object[] eklenecek = {personel.getId(), personel.getPersonelAd(), personel.getPersonelSoyad(), personel.getEmail(), personel.getSifre(), personel.getDepartman()};
                
                model.addRow(eklenecek);
            }
        }
    }
    
