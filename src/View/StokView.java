    public void StokGoruntule(){
        model.setRowCount(0);
        
        ArrayList<Stok> stoklar = new ArrayList<Stok>();
        
        stoklar = veriTabani.stokGetir();
        
        if(stoklar != null){
            for(Stok stok : stoklar){
                Object[] eklenecek = {stok.getId(),stok.getUrunId(),stok.getSatilanMiktar(),stok.getAlinanMiktar()};
                
                model.addRow(eklenecek);
            }
        }
        
    }
    
