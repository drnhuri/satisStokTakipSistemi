    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tableRow = new TableRowSorter<DefaultTableModel>(model);
        
        personelTable.setRowSorter(tableRow);
        
        tableRow.setRowFilter(RowFilter.regexFilter(ara));
    }
    
