    public void dinamikAra(String ara){
        
        TableRowSorter<DefaultTableModel> tableRow = new TableRowSorter<DefaultTableModel>(model);
        
        urunTbl.setRowSorter(tableRow);
        
        tableRow.setRowFilter(RowFilter.regexFilter(ara));
    }
    
    private void aramaCubuguTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_aramaCubuguTextKeyReleased
        
        //klavyeden arama işlemi bitince getirir.
        
        String ara = aramaCubuguText.getText();
        
        dinamikAra(ara);
        
    }//GEN-LAST:event_aramaCubuguTextKeyReleased

    private void silBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silBtnActionPerformed
        
        mesaj2Lbl.setText("");
        
        int selectedrow = urunTbl.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                mesaj2Lbl.setText("Ürün tablosu şu anda boş");
            }
            else{
                mesaj2Lbl.setText("Lütfen silinecek bir ürün seçiniz.");
            }
        }
        else{
            int id = (int) model.getValueAt(selectedrow, 0);
            
            veriTabani.urunSil(id);
            
            UrunGoruntule();
            mesaj2Lbl.setText("Ürün başarıyla silindi");
        }
    }//GEN-LAST:event_silBtnActionPerformed

