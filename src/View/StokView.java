    private void ekleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleBtnActionPerformed
        
        mesajText.setText("");
        
        String id = idText.getText();
        String urunId = urunIdText.getText();
        String satilanMiktar = satilanMiktarText.getText();
        String alinanMiktar = alinanMiktarText.getText();
        
        veriTabani.stokEkle(id,urunId,satilanMiktar,alinanMiktar);
        StokGoruntule();
        mesajText.setText("Yeni stok başarıyla eklendi.");
    }//GEN-LAST:event_ekleBtnActionPerformed

    private void silBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silBtnActionPerformed
        
        mesajText.setText("");
        
        int selectedrow = stokTbl.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                mesajText.setText("Stok tablosu şu anda boş");
            }
            else{
                mesajText.setText("Lütfen silinecek bir stok seçiniz");
            }
        }
        else{
            int id = (int) model.getValueAt(selectedrow,0);
            
            veriTabani.stokSil(id);
            StokGoruntule();
            mesajText.setText("Stok başarıyla silindi");
        }
    }//GEN-LAST:event_silBtnActionPerformed

