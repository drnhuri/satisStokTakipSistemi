    private void stokTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stokTblMouseClicked
        
        int selectedRow = stokTbl.getSelectedRow();
        idText.setText(model.getValueAt(selectedRow,0).toString());
        urunIdText.setText(model.getValueAt(selectedRow,1).toString());
        satilanMiktarText.setText(model.getValueAt(selectedRow, 2).toString());
        alinanMiktarText.setText(model.getValueAt(selectedRow, 3).toString());
    }//GEN-LAST:event_stokTblMouseClicked

    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tableRow = new TableRowSorter<DefaultTableModel>();
        stokTbl.setRowSorter(tableRow);
        tableRow.setRowFilter(RowFilter.regexFilter(ara));
    }
    
